package com.internousdev.mocha.dto;

public class MCategoryDTO {
	public String product_id;

	public String product_name;

	public String product_name_kana;

	public String image_file_path;

	public String price;

	public String category_id;

	public String category_name;

	public String getProductId() {
		return product_id;
	}

	public void setProductId(String product_id) {
		this.product_id = product_id;
	}

	public String getProductName() {
		return product_name;
	}

	public void setProductName(String product_name) {
		this.product_name = product_name;
	}

	public String getProductNameKana() {
		return product_name_kana;
	}

	public void setProductNameKana(String product_name_kana) {
		this.product_name_kana = product_name_kana;
	}

	public String getImageFilePath() {
		return image_file_path;
	}

	public void setImageFilePath(String image_file_path) {
		this.image_file_path = image_file_path;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getCategoryId() {
		return category_id;
	}

	public void setCategoryId(String category_id) {
		this.category_id = category_id;
	}

	public String getCategoryName() {
		return category_name;
	}

	public void setCategoryName(String category_name) {
		this.category_name = category_name;
	}

}
